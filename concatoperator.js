//concat method is used to concat or merge an element in an array or you can merge two arrays together.
//concat method can be apply to string only.
//concat joins two or more strings together.
//concat returns a new array.
//concat in strings:-
var string1 = "Hello";
var string2 = "Huma";
console.log(string1.concat(" ", string2)); //returns a new string.
//------------------------------------------------------------
//concat in an array:-
var grade_1 = ['ali', 'ahmed', 'zain', 'kashif'];
var grade_2 = ['amna', 'zimmal', 'natasha'];
grade_1 = grade_1.concat("ibrahim");
console.log(grade_1);
var Total_students = grade_1.concat(grade_2);
console.log(Total_students); //returns a new array
